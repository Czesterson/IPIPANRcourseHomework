library("testthat")
expect_error(IPIPANRcourseHomework::simplify2array2("a"))
expect_error(IPIPANRcourseHomework::simplify2array2(data.frame()))
expect_error(IPIPANRcourseHomework::simplify2array2(list(2:10, c("f", "G"), 5:2)))

ex <- list(1, 2:3, 4:10)
expect_equal(simplify2array(ex), IPIPANRcourseHomework::simplify2array2(ex))
ex <- list(7, 3, 2:3, -10)
expect_equal(simplify2array(ex), IPIPANRcourseHomework::simplify2array2(ex))
ex <- list(7, 3, 2:2, -10)
expect_equal(simplify2array(ex), IPIPANRcourseHomework::simplify2array2(ex))
ex <- list(c(42), c(0.1e31), c(-5), c(1.1))
expect_equal(simplify2array(ex), IPIPANRcourseHomework::simplify2array2(ex))
ex <- list(-20:-50)
expect_equal(simplify2array(ex), IPIPANRcourseHomework::simplify2array2(ex))
ex <- list(c(3, 2.2, -1))
expect_equal(simplify2array(ex), IPIPANRcourseHomework::simplify2array2(ex))
ex <- list(5.3, 3.2, 1.7, 4.4, -2.1, -1.3e-2)
expect_equal(simplify2array(ex), IPIPANRcourseHomework::simplify2array2(ex))
ex <- list(12:14, 24:26, 36:38, 48:50)
expect_equal(simplify2array(ex), IPIPANRcourseHomework::simplify2array2(ex))
ex <- list(c(4,3,2,6,7), c(-1, -4, -2, -3.2, -2))
expect_equal(simplify2array(ex), IPIPANRcourseHomework::simplify2array2(ex))
ex <- list(c(2,7), c(32, -22), c(0,0), c(0,0), c(0,2), c(214324532, 32523532), c(-24, -2142154235), c(0, -42), c(33, 33))
expect_equal(simplify2array(ex), IPIPANRcourseHomework::simplify2array2(ex))

